..................... Vistas Rails

Las vistas son muy similares entre Sinatra y Rails, la única diferencia 
es que dentro del controlador en vez de utilizar erb, rails usa un 
método más inteligente llamado render, de esta manera no importa el 
formato en el que este la vista.

Ya vimos cuales son las diferencias de los helpers en Rails, pero 
olvidamos mencionarte algo importante. Rails tiene integrado varios 
helpers que te facilitarán el desarrollo de tus aplicaciones mucho. 
Algunos de ellos son los siguientes:

link_to
button_to
image_tag
form_for.


